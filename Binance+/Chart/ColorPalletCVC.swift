    override func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return 125
    }

    override func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: reuseIdentifier, for: indexPath)

        let values = [0, 64, 128, 192, 255]
        
        let i = indexPath.row
        
        let rIndex = (i % 5)
        let gIndex = (i / 5) % 5
        let bIndex = (i / 25) % 5
        
        let r = CGFloat(values[rIndex]) / 255
        let g = CGFloat(values[gIndex]) / 255
        let b = CGFloat(values[bIndex]) / 255
        
        cell.backgroundColor = UIColor.init(red: r, green: g, blue: b, alpha: 1.0)
    
        return cell
    }

    // MARK: UICollectionViewDelegate

    override func collectionView(_ collectionView: UICollectionView, didSelectItemAt indexPath: IndexPath) {
        chartVC.colorPalletCVCCompletion?(collectionView.cellForItem(at: indexPath)?.backgroundColor)
    }
    
